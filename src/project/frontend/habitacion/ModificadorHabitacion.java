    private void buttonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModificarActionPerformed
        try {
            ManejadorHabitacion.getInstance().updateHabitacion(habitacionSelec, formattedTextFieldPrecio.getText().trim(), formattedTextFieldCosto.getText().trim());
            JOptionPane.showMessageDialog(rootPane, "Se ha actualizado correctamente");
            actualizarLista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        buttonModificar.setEnabled(false);
    }//GEN-LAST:event_buttonModificarActionPerformed

    private void actualizarLista() {
        listaHabtObser.clear();
        if(ManejadorHabitacion.getInstance().getHabitaciones() != null){
            listaHabtObser.addAll(ManejadorHabitacion.getInstance().getHabitaciones());
        }
        buttonModificar.setEnabled(false);
    }

